num = int(input("Digite um número"))

if num == 1:
    print("Domingo")

if num == 2:
    print("Segunda")

if num == 3:
    print("Terça")

if num == 4:
    print("Quarta")

if num == 5:
    print("Quinta")

if num == 6:
    print("Sexta")

if num == 7:
    print("Sábado")

else:
    print("Nao existe dia da semana com esse numero!")